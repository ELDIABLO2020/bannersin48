/**
 * E2E design audit: compare live Housecall Pro vs local Banners In 48 homepage.
 * Usage: node frontend/scripts/audit-hcp-design.mjs [--local=http://localhost:3001]
 */
import { chromium } from "@playwright/test";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ARTIFACTS = join(__dirname, "../e2e/artifacts/hcp-audit");
const REPORT_PATH = join(__dirname, "../../docs/design-audit-hcp-2026-06-20.md");

const HCP_URL = "https://www.housecallpro.com/";
const LOCAL_URL =
  process.argv.find((a) => a.startsWith("--local="))?.split("=")[1] ??
  process.env.LOCAL_URL ??
  "http://localhost:3000";

const VIEWPORTS = [
  { name: "mobile", width: 375, height: 812 },
  { name: "tablet", width: 1024, height: 768 },
  { name: "desktop", width: 1440, height: 900 },
];

function rgbToHex(rgb) {
  if (!rgb || rgb === "transparent" || rgb === "rgba(0, 0, 0, 0)") return null;
  const m = rgb.match(/rgba?\((\d+),\s*(\d+),\s*(\d+)(?:,\s*([\d.]+))?\)/);
  if (!m) return rgb;
  const hex = (n) => Number(n).toString(16).padStart(2, "0");
  if (m[4] !== undefined && Number(m[4]) < 1) return rgb;
  return `#${hex(m[1])}${hex(m[2])}${hex(m[3])}`.toLowerCase();
}

async function dismissHcpModals(page) {
  for (const text of ["No, stay on this page", "Close", "×"]) {
    const btn = page.getByRole("button", { name: new RegExp(text, "i") }).first();
    if (await btn.isVisible().catch(() => false)) await btn.click().catch(() => {});
  }
}

async function extractPageMetrics(page, label) {
  return page.evaluate((siteLabel) => {
    const rgbToHexLocal = (rgb) => {
      if (!rgb || rgb === "transparent" || rgb === "rgba(0, 0, 0, 0)") return null;
      const m = rgb.match(/rgba?\((\d+),\s*(\d+),\s*(\d+)(?:,\s*([\d.]+))?\)/);
      if (!m) return rgb;
      const hex = (n) => Number(n).toString(16).padStart(2, "0");
      if (m[4] !== undefined && Number(m[4]) < 1) return rgb;
      return `#${hex(m[1])}${hex(m[2])}${hex(m[3])}`.toLowerCase();
    };

    const h1 = document.querySelector("h1");
    const h2 = document.querySelector("section h2, main h2");
    const h1Style = h1 ? getComputedStyle(h1) : null;
    const h2Style = h2 ? getComputedStyle(h2) : null;

    const cta = [...document.querySelectorAll("a, button")].find((el) =>
      /start (free trial|your order)/i.test(el.textContent || ""),
    );
    const ctaStyle = cta ? getComputedStyle(cta) : null;

    const imgs = document.querySelectorAll("img").length;
    const bgImages = [...document.querySelectorAll("*")].filter(
      (el) => getComputedStyle(el).backgroundImage !== "none",
    ).length;

    const mockStrings = [
      "YOUR BANNER HERE",
      "Product UI preview",
      "Customer story",
      "Storefront banner",
      "Mobile order view",
    ];
    const mockHits = mockStrings.filter((s) => document.body.innerText.includes(s));

    const colorCounts = new Map();
    for (const el of document.querySelectorAll("*")) {
      const cs = getComputedStyle(el);
      const bg = rgbToHexLocal(cs.backgroundColor);
      if (bg) colorCounts.set(bg, (colorCounts.get(bg) || 0) + 1);
    }
    const topColors = [...colorCounts.entries()].sort((a, b) => b[1] - a[1]).slice(0, 10);

    return {
      site: siteLabel,
      heroH1: h1Style
        ? {
            fontFamily: h1Style.fontFamily,
            fontSize: h1Style.fontSize,
            color: rgbToHexLocal(h1Style.color),
          }
        : null,
      sectionH2: h2Style
        ? {
            fontFamily: h2Style.fontFamily,
            fontSize: h2Style.fontSize,
          }
        : null,
      cta: ctaStyle
        ? {
            text: (cta.textContent || "").trim().slice(0, 40),
            bg: rgbToHexLocal(ctaStyle.backgroundColor),
            color: rgbToHexLocal(ctaStyle.color),
            borderRadius: ctaStyle.borderRadius,
            height: ctaStyle.height,
            padding: ctaStyle.padding,
          }
        : null,
      imageCount: imgs,
      bgImageCount: bgImages,
      mockPlaceholderHits: mockHits,
      topColors,
    };
  }, label);
}

function buildReport(hcpResults, localResults, checks) {
  const lines = [
    "# HCP Design Audit — 2026-06-20",
    "",
    `Generated by \`audit-hcp-design.mjs\`.`,
    "",
    "## Targets",
    `- Reference: [housecallpro.com](${HCP_URL})`,
    `- Local: ${LOCAL_URL}`,
    "",
    "## Summary",
    "",
    "| Check | Status |",
    "|-------|--------|",
    ...checks.map((c) => `| ${c.name} | ${c.pass ? "PASS" : "FAIL"} |`),
    "",
    "## Viewport Metrics",
    "",
  ];

  for (const vp of VIEWPORTS) {
    lines.push(`### ${vp.name} (${vp.width}px)`);
    lines.push("");
    lines.push("**Housecall Pro**");
    lines.push("```json");
    lines.push(JSON.stringify(hcpResults[vp.name], null, 2));
    lines.push("```");
    lines.push("");
    lines.push("**Banners In 48**");
    lines.push("```json");
    lines.push(JSON.stringify(localResults[vp.name], null, 2));
    lines.push("```");
    lines.push("");
    lines.push(`Screenshots: \`frontend/e2e/artifacts/hcp-audit/hcp-${vp.name}.png\`, \`local-${vp.name}.png\``);
    lines.push("");
  }

  lines.push("## Section Checklist (Local vs HCP patterns)");
  lines.push("");
  lines.push("| Section | Parity notes |");
  lines.push("|---------|--------------|");
  for (const c of checks) {
    lines.push(`| ${c.name} | ${c.note} |`);
  }

  return lines.join("\n");
}

mkdirSync(ARTIFACTS, { recursive: true });
mkdirSync(dirname(REPORT_PATH), { recursive: true });

const browser = await chromium.launch();
const hcpResults = {};
const localResults = {};

for (const vp of VIEWPORTS) {
  const page = await browser.newPage({ viewport: { width: vp.width, height: vp.height } });

  await page.goto(HCP_URL, { waitUntil: "networkidle", timeout: 90000 });
  await page.waitForTimeout(2000);
  await dismissHcpModals(page);
  hcpResults[vp.name] = await extractPageMetrics(page, "hcp");
  await page.screenshot({ path: join(ARTIFACTS, `hcp-${vp.name}.png`), fullPage: true });

  await page.close();
}

let localOk = true;
for (const vp of VIEWPORTS) {
  const page = await browser.newPage({ viewport: { width: vp.width, height: vp.height } });
  try {
    await page.goto(LOCAL_URL, { waitUntil: "networkidle", timeout: 60000 });
    await page.waitForTimeout(1500);
    localResults[vp.name] = await extractPageMetrics(page, "local");
    await page.screenshot({ path: join(ARTIFACTS, `local-${vp.name}.png`), fullPage: true });
  } catch (err) {
    localOk = false;
    localResults[vp.name] = { error: String(err) };
  }
  await page.close();
}

await browser.close();

const desktopLocal = localResults.desktop ?? {};
const checks = [
  {
    name: "CTA gold (#FF9B24)",
    pass: desktopLocal.cta?.bg === "#ff9b24",
    note: desktopLocal.cta?.bg ?? "CTA not found",
  },
  {
    name: "No blank mock strings",
    pass: (desktopLocal.mockPlaceholderHits?.length ?? 0) === 0,
    note: desktopLocal.mockPlaceholderHits?.join(", ") || "none",
  },
  {
    name: "Hero/product images present",
    pass: (desktopLocal.imageCount ?? 0) >= 5,
    note: `${desktopLocal.imageCount ?? 0} img elements`,
  },
  {
    name: "Display typography loaded",
    pass: desktopLocal.heroH1?.fontFamily?.toLowerCase().includes("bebas") ?? false,
    note: desktopLocal.heroH1?.fontFamily ?? "missing",
  },
  {
    name: "Local server reachable",
    pass: localOk,
    note: localOk ? LOCAL_URL : `Failed to load ${LOCAL_URL}`,
  },
];

const report = buildReport(hcpResults, localResults, checks);
writeFileSync(REPORT_PATH, report, "utf8");
console.log(`Audit report → ${REPORT_PATH}`);
console.log(`Screenshots → ${ARTIFACTS}`);
console.log(JSON.stringify({ checks: checks.map((c) => ({ name: c.name, pass: c.pass })) }, null, 2));
